package com.springapp.mvc.data;


import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import java.math.BigDecimal;
import java.math.BigInteger;
import java.util.Date;

/**
 * Created by RMA on 3/24/2020.
 */

@Entity
@Table(name="dfrs_loan_detail")
public class FileUploadDetail {

    @Id
    @Column(name="loan_detail_id")
    private BigInteger loan_detail_id;

    @Column(name="bank_id")
    private BigInteger bank_id;

    @Column(name="sector_id")
    private BigInteger sector_id;

    @Column(name="subsector_id")
    private BigInteger subsector_id;

    @Column(name="nos_account")
    private Integer nos_account;

    @Column(name="principal_outstanding")
    private BigDecimal principal_outstanding;

    @Column(name="interest_outstanding")
    private BigDecimal interest_outstanding;

    @Column(name="pri_int_overdue")
    private BigDecimal pri_int_overdue;

    @Column(name="createdby")
    private String createdby;

    @Column(name="createdon")
    private Date createdon;

    @Column(name="modifiedby")
    private String modifiedby;

    @Column(name="modifiedon")
    private Date modifiedon;

    public BigInteger getLoan_detail_id() {
        return loan_detail_id;
    }

    public void setLoan_detail_id(BigInteger loan_detail_id) {
        this.loan_detail_id = loan_detail_id;
    }

    public BigInteger getBank_id() {
        return bank_id;
    }

    public void setBank_id(BigInteger bank_id) {
        this.bank_id = bank_id;
    }

    public BigInteger getSector_id() {
        return sector_id;
    }

    public void setSector_id(BigInteger sector_id) {
        this.sector_id = sector_id;
    }

    public BigInteger getSubsector_id() {
        return subsector_id;
    }

    public void setSubsector_id(BigInteger subsector_id) {
        this.subsector_id = subsector_id;
    }

    public Integer getNos_account() {
        return nos_account;
    }

    public void setNos_account(Integer nos_account) {
        this.nos_account = nos_account;
    }

    public BigDecimal getPrincipal_outstanding() {
        return principal_outstanding;
    }

    public void setPrincipal_outstanding(BigDecimal principal_outstanding) {
        this.principal_outstanding = principal_outstanding;
    }

    public BigDecimal getInterest_outstanding() {
        return interest_outstanding;
    }

    public void setInterest_outstanding(BigDecimal interest_outstanding) {
        this.interest_outstanding = interest_outstanding;
    }

    public BigDecimal getPri_int_overdue() {
        return pri_int_overdue;
    }

    public void setPri_int_overdue(BigDecimal pri_int_overdue) {
        this.pri_int_overdue = pri_int_overdue;
    }

    public String getCreatedby() {
        return createdby;
    }

    public void setCreatedby(String createdby) {
        this.createdby = createdby;
    }

    public Date getCreatedon() {
        return createdon;
    }

    public void setCreatedon(Date createdon) {
        this.createdon = createdon;
    }

    public String getModifiedby() {
        return modifiedby;
    }

    public void setModifiedby(String modifiedby) {
        this.modifiedby = modifiedby;
    }

    public Date getModifiedon() {
        return modifiedon;
    }

    public void setModifiedon(Date modifiedon) {
        this.modifiedon = modifiedon;
    }
}
